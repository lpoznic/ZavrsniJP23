    private class Vrijeme extends Thread{
        
        private SimpleDateFormat df = 
                new SimpleDateFormat("dd. MMMM YYYY. HH:mm:ss");

        @Override
        public void run() {
            while (true) {                
                lblVrijeme.setText(df.format(new Date()));
                try {
                    Thread.sleep(1000);
                } catch (InterruptedException ex) {
                }
            }
        }
        
    }
    
    private class NarudzbaPane{
        
        private void ucitajNarudzba() {

        DefaultListModel<Narudzba> m = new DefaultListModel<>();

        m.addAll(obradaN.getPodaci());

        lstNarudzbe.setModel(m);

    }

    private void postaviVrijednostiNaEntitet() {
        var entitet=obradaN.getEntitet();
        
        entitet.setDatum(new Date());

        try {
            entitet.setKupac();
        } catch (Exception e) {
            entitet.setKupac(null);
        }

        try {
            entitet.setUkupnaCijena(new BigDecimal(txtUkCijena.getText()));
        } catch (Exception e) {
            entitet.setUkupnaCijena(BigDecimal.ZERO);
        }
        
        try {
            entitet.setBrojRacuna(Integer.parseInt(txtBrojRacuna.getText()));
        } catch (Exception e) {
            entitet.setBrojRacuna(0);
        }
        
        
        try {
            entitet.setUkupnaCijena(new BigDecimal(txtUkCijena.getText()));
        } catch (Exception e) {
            entitet.setUkupnaCijena(BigDecimal.ZERO);
        }
        try {
            entitet.setUkupnaCijena(new BigDecimal(txtUkCijena.getText()));
        } catch (Exception e) {
            entitet.setUkupnaCijena(BigDecimal.ZERO);
        }

    }

    private void pocisti() {
        txtBrojRacuna.setText("");
        txtUkCijena.setText("");
        txtDatum.setText("");
        txtKupac.setText("");
        txtZaposlenik.setText("");
        

    }
        
    }
    
